                  current_char = self.ascii_string[index]
                  x = Deasciifier.turkish_downcase_asciify_table.get(current_char
                                                                     ,False)
                  if not x:
                        if not space:
                              i = i + 1
                              space = True
                  else:
                        s = s[0:i] + x + s[i+1:]
                        i = i + 1
                        space = False
                  index = index + 1
            
